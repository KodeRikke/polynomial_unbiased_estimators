def plot_plots(
    *,
    q_values,
    q_range,
    epsilon_values,
    epsilon_range,
    polynomials,
    Delta_values,
    delta_value,
    metrics,
    each_plot,
    path_for_plots
):
    """
    Main function to generate and save the desired plots.
    Input:
    - q_values: a list of specific q values to plot (if plotting over epsilon)
    - q_range: a tuple (q_min, q_max) specifying the range of q values to plot (if plotting over q)
    - epsilon_values: a list of specific epsilon values to plot (if plotting over q)
    - epsilon_range: a tuple (epsilon_min, epsilon_max) specifying the range of epsilon values to plot (if plotting over epsilon)
    - polynomials: a dictionary mapping polynomial names to their sympy expressions
    - Delta_values: a list of specific Delta values to use in the plots
    - delta_value: a specific value of delta to use in the Gaussian noise calibration (if needed)
    - metrics: a list of metric names to plot (e.g., "variance_ratio", "mse_relative", etc.)
    - each_plot: a list of strings indicating how to group curves in each plot (e.g., "q_by_poly", "poly_by_q", etc.)
    - path_for_plots: the base directory path where the generated plots should be saved
    Output:
    - The function generates and saves the specified plots according to the input parameters.
    """

    # Build symbolic reports for both noise models
    reports_by_noise_dist, symbols_by_noise_dist = build_reports(polynomials)

    eps_grid = np.linspace(epsilon_range[0], epsilon_range[1], 300)
    q_grid = np.linspace(q_range[0], q_range[1], 300)

    names = list(polynomials.keys())

    for metric in metrics:
        y_label = METRIC_LABELS[metric]

        # One plot per polynomial, curves vary by q values and noise model, x = eps
        if "q_by_poly" in each_plot:
            for name in names:
                for Delta_value in Delta_values:
                    curves = curves_q_for_poly(
                        reports=reports_by_noise_dist,
                        symbols=symbols_by_noise_dist,
                        name=name,
                        metric=metric,
                        q_values=q_values,
                        Delta_value=Delta_value,
                        eps_grid=eps_grid, 
                        delta_value=delta_value
                    )
                    title = (
                        f"{y_label} vs ε, for {format_value(name)}, " 
                        f"Δ = {format_value(Delta_value)}"
                    )
                    save_path = os.path.join(
                        path_for_plots, 
                        "q_by_poly",
                        metric_folder(metric)
                    )
                    " x_label, y_label, x_grid, y_data, title=None, save_path=None"
                    plot_curves(x_label="ε", 
                                y_label=y_label,
                                x_grid=eps_grid,
                                y_data=curves,
                                title=title,
                                save_path=save_path
                    )
        # One plot per q value, curves vary by polynomial and noise model, x = eps
        if "poly_by_q" in each_plot:
            for q_value in q_values:
                for Delta_value in Delta_values:
                    curves = curves_poly_for_q(
                        reports=reports_by_noise_dist,
                        symbols=symbols_by_noise_dist,
                        names=names,
                        metric=metric,
                        q_value=q_value,
                        Delta_value=Delta_value,
                        eps_grid=eps_grid,
                        delta_value=delta_value
                    )
                    title = (
                        f"{y_label} vs ε, for q = {format_value(q_value)}, "
                        f"Δ = {format_value(Delta_value)}"
                    )
                    save_path = os.path.join(
                        path_for_plots, 
                        "poly_by_q", 
                        metric_folder(metric)
                    )
                    plot_curves(x_label="ε", 
                                y_label=y_label, 
                                x_grid=eps_grid, 
                                y_data=curves, 
                                title=title, 
                                save_path=save_path
                    )
        # One plot per polynomial, curves vary by epsilon values and noise model, x = q
        if "epsilon_by_poly" in each_plot:
            for name in names:
                for Delta_value in Delta_values:
                    curves = curves_epsilon_for_poly(
                        reports=reports_by_noise_dist,
                        symbols=symbols_by_noise_dist,
                        name=name,
                        metric=metric,
                        epsilon_values=epsilon_values,
                        Delta_value=Delta_value,
                        q_grid=q_grid,
                        delta_value=delta_value
                    )
                    title = (
                        f"{y_label} vs ε, for {format_value(name)}, "
                        f"Δ = {format_value(Delta_value)}"
                    )
                    save_path = os.path.join(
                        path_for_plots, 
                        "epsilon_by_poly", 
                        metric_folder(metric)
                    )
                    plot_curves(x_label="q", 
                                y_label=y_label, 
                                x_grid=q_grid, 
                                y_data=curves, 
                                title=title, 
                                save_path=save_path
                    )