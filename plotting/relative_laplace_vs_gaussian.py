def curves_q_for_poly(
        reports, # reports by noise distribution
        symbols, # symbls by noise distribution
        name,
        metric,
        q_values, 
        Delta_value,
        eps_grid, 
        delta_value
):
    """
    Build curves of the given metric. 
    One plot for each polynomial, 
    curves vary by q values,  
    plotted over epsilon on the x-axis, 
    and with fixed Delta, 
    for both noise distributions.

    OBS! This function only gets a single polynomial name as input, so this is meant to be
    called in a loop over polynomial names, to generate one plot per polynomial.

    Input:
    - reports: a dictionary mapping noise distributions to their respective symbolic reports, as returned by build_reports
    - symbols: a dictionary mapping noise distributions to their respective dictionaries of sympy symbols, as returned by build_reports
    - name: the name of the polynomial to plot (must be a key in the reports)
    - metric: the name of the metric to plot (e.g., "variance_ratio", "mse_relative", etc.)
    - q_values: a list of specific q values to plot
    - Delta_value: a specific value of Delta to use in the plots
    - eps_grid: a numpy array of epsilon values to evaluate the curves on
    Output:
    """
    out = {}
    
    for noise_dist in ["laplace", "gaussian"]:
        expr = metric_expr(
            reports_by_label=reports[noise_dist],
            label=name,
            metric=metric
        )

        for q_value in q_values:
            subs = base_subs_for_noise(
                noise_dist=noise_dist, 
                symbols=symbols, 
                q_value=q_value, 
                Delta_value=Delta_value,
                delta_value=delta_value
            )
            label = f"{noise_dist.capitalize()} noise, q={format_value(q_value)}"
            out[label] = evaluate_on_grid(expr, 
                                          # always using epsilon from laplace, as epsilon does
                                          # NOT exist for Gaussian noise, but is introduced via 
                                          # the relation sigma = sqrt(2) * Delta / epsilon
                                          symbols["laplace"]["epsilon"],
                                          eps_grid, 
                                          subs
                                          )
    return out

    

