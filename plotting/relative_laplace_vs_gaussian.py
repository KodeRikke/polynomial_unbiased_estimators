def build_system(noise_model="laplace"):
    """
    Build the EstimatorSystem with the desired noise models and symbols. 
    Input: None
    Output: Two tuples (system, symbols), where
    - system is an instance of EstimatorSystem, initialized with the desired noise
      model and symbols
    - symbols is a dictionary of sympy symbols used in the system
    """
    q = sp.symbols("q", positive=True, real=True)
    X = sp.symbols("X", positive=True, real=True)

    if noise_model == "laplace":
        Delta = sp.symbols("Delta", positive=True, real=True)
        epsilon = sp.symbols("epsilon", positive=True, real=True)

        system = EstimatorSystem(
            noise_model=LaplaceNoiseModel(Delta=Delta, epsilon=epsilon),
            q=q,
            x=X
        )
        symbols = {"Delta": Delta, "epsilon": epsilon, "q": system.q, "X": system.x}

    elif noise_model == "gaussian":
        sigma = sp.symbols("sigma", positive=True, real=True)

        system = EstimatorSystem(
            noise_model=GaussianNoiseModel(sigma=sigma),
            q=q,
            x=X
        )
        symbols = {"sigma": sigma, "q": system.q, "X": system.x}

    else:
        raise ValueError(f"Unsupported noise model: {noise_model}")
    
    return system, symbols

def symbolic_poly_reports(system, polynomials):
    """
    Generate symbolic reports for a list of polynomials, using the given EstimatorSystem.
    Input:
    - system: an instance of EstimatorSystem, initialized with the desired noise
      model and symbols
    - polynomials: a list of tuples (name, f), where name is a string identifier 
      for the polynomial, and f is a sympy expression for the polynomial function of q.
    Output:
    - A dictionary mapping each polynomial name to its symbolic report 
      generated by the system's compare method
    """
    reports = {}
    for name, f in polynomials.items():

        # The following is to avoid issues with sympy not recognizing the symbol
        # "q" in the symbolic expressions,whic can happen with wrong symbols. 
        f = sp.sympify(f)
        # Replace any symbol named "q" with system.q.
        for sym in f.free_symbols:
            if sym.name == "q":
                f = f.subs(sym, system.q)

        reports[name] = system.compare(f=f)
    return reports

def build_reports(polynomials):
    """
    Build the EstimatorSystem's and generate symbolic reports for the given polynomials.
    Input:
    - polynomials: a list of tuples (name, f), where name is a string identifier 
      for the polynomial, and f is a sympy expression for the polynomial function of q.
    Output:
    - A dictionary mapping the noise models, "laplace" and "gaussian", to their respective 
        dictionaries of symbolic reports and their symbols. Specifically, the output is a tuple of two dictionaries:
        - The first dictionary maps each noise model ("laplace" and "gaussian") to its respective dictionary of symbolic report.
        - The second dictionary maps each noise model to its respective dictionary of sympy symbols used in the system.

    """
    laplace_system, laplace_symbols = build_system(noise_model="laplace")
    gaussian_system, gaussian_symbols = build_system(noise_model="gaussian")

    laplace_reports = symbolic_poly_reports(laplace_system, polynomials)
    gaussian_reports = symbolic_poly_reports(gaussian_system, polynomials)

    return {
        "laplace": laplace_reports,
        "gaussian": gaussian_reports
    }, {
        "laplace": laplace_symbols,
        "gaussian": gaussian_symbols
    }

def base_subs_for_noise(
        noise_dist, 
        symbols, 
        *, 
        q_value=None, 
        Delta_value=None, 
        epsilon_value=None,
        delta_value=None
):
    """
    Build substitution dicts for Gaussian or Laplace noise. 
    For Gaussian, epsilon is introduced via the relation: 
        sigma = sqrt(2) * Delta / epsilon, 
    as it is not an inherent parameter ot this noise distribution. 

    If epsilon_value is None, sigma remains symbolic in epsilon
    (for plotting over epsilon on the x-axis). 

    Input: 
    - noise_dist: a string, either "laplace or "gaussian", 
    - symbols: the dictionary of symbols ordered by noise distribution, 
      as returned by the build_reports function, i.e., symbols_by_noise_dist
    - q_value: a specific value of q to substitute (if not None)
    - Delta_value: a specific value of Delta to substitute (if not None)
    - epsilon_value: a specific value of epsilon to substitute (if not None)
    Output:
    - A dictionary of substitutions to apply to the symbolic expressions for the given noise distribution.
    """
    # ----- Laplace -----
    if noise_dist == "laplace":
        subs = {}
        if q_value is not None:
            subs[symbols[noise_dist]["q"]] = q_value
        if Delta_value is not None:
            subs[symbols[noise_dist]["Delta"]] = Delta_value
        if epsilon_value is not None:            
            subs[symbols[noise_dist]["epsilon"]] = epsilon_value

    # ----- Gaussian -----
    elif noise_dist == "gaussian":
        subs = {}
        if q_value is not None:
            subs[symbols[noise_dist]["q"]] = q_value

        if Delta_value is not None:
            # epsilon is symbolic and symbolic report construct id being done
            if epsilon_value is None:
                # the value epsilon is NOT inherent in Gaussian dist
                epsilon_symbol = symbols["laplace"]["epsilon"]
                subs[symbols[noise_dist]["sigma"]] = SigmaFromEpsilon(
                    epsilon_symbol,
                    delta_value,  # a very small delta to approximate pure DP
                    Delta_value
                )
            # epsilon is numeric (float, int, Rational, etc)
            else:
                 # turn the value of epsilon into a float
                 # to ensure it can be passed to the numerical func
                 eps_num = float(epsilon_value)
                 sigma_val = SigmaFromEpsilon.numeric(
                    eps_num,
                    delta_value,  # a very small delta to approximate pure DP
                    float(Delta_value))
                 subs[symbols[noise_dist]["sigma"]] = sigma_val
    else:
        raise ValueError(f"Unsupported noise distribution: {noise_dist}")
    return subs

