def curves_q_for_fixed_degree(
        reports_by_degree, 
        degree, 
        q_values, 
        metric, 
        epsilon_symbol, 
        eps_grid, 
        q_symbol, 
        base_subs
):
    """
    One plot for a fixed degree.
    Curves vary by q.
    X-axis is epsilon.
    """
    out = {}
    expr = metric_expr(reports_by_degree, degree, metric)

    for q_value in q_values:
        subs = dict(base_subs) # make a copy of the base substitutions, to avoid modifying the original
        subs[q_symbol] = q_value
        label = f"q = {format_value(q_value)}"
        out[label] = evaluate_on_grid(expr, epsilon_symbol, eps_grid, subs)
    
    return out

def curves_degree_for_fixed_q(
        reports_by_degree, 
        degree_values, 
        q_value, 
        metric, 
        epsilon_symbol, 
        eps_grid, 
        q_symbol, 
        base_subs
):
    """
    One plot for a fixed q.
    Curves vary by degree.
    X-axis is epsilon.
    """
    out = {}
    subs = dict(base_subs) # make a copy of the base substitutions, to avoid modifying the original
    subs[q_symbol] = q_value

    for degree in degree_values:
        expr = metric_expr(reports_by_degree, degree, metric)
        label = f"Degree {format_value(degree)}"
        out[label] = evaluate_on_grid(expr, epsilon_symbol, eps_grid, subs)
    
    return out

def curves_epsilon_for_fixed_degree(
        reports_by_degree,
        degree, 
        epsilon_values, 
        metric, 
        q_symbol, 
        q_grid, 
        epsilon_symbol,
        base_subs
):
    """
    One plot for a fixed degree.
    Curves vary by epsilon.
    X-axis is q.
    """
    out = {}
    expr = metric_expr(reports_by_degree, degree, metric)

    for epsilon_value in epsilon_values:
        subs = dict(base_subs) # make a copy of the base substitutions, to avoid modifying the original
        subs[epsilon_symbol] = epsilon_value
        label = f"epsilon = {format_value(epsilon_value)}"
        out[label] = evaluate_on_grid(expr, q_symbol, q_grid, subs)
    
    return out

