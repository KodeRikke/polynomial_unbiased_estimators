def build_system():
    """
    Function initializes the EstimatorSystem with a Laplace noise model, using symbolic variables for Delta, epsilon, q, and X.
    This allows for symbolic analysis of the estimators and their properties as functions of these parameters. 
    Input: 
    - None (the symbolic variables are defined within the function)
    Output:
    - system: an instance of EstimatorSystem initialized with the Laplace noise model and symbolic variables
    - {"Delta": Delta, "epsilon": epsilon, "q": q, "X": X}: a dictionary of the symbolic variables used in the system.
    """
    Delta = sp.Symbol("Delta", real=True, positive=True)
    epsilon = sp.Symbol("epsilon", real=True, positive=True)
    q = sp.Symbol("q", real=True)
    X = sp.Symbol("X", real=True)

    system = EstimatorSystem(
        noise_model=LaplaceNoiseModel(delta=Delta, epsilon=epsilon),
        q=q,
        x=X
    )
    return system, {"Delta": Delta, "epsilon": epsilon, "q": q, "X": X}

def symbolic_chebyshev_reports(system, chebyshev_degrees):
    """
    Function takes an EstimatorSystem and a list of Chebyshev degrees, and computes 
    the comparison reports for the unbiased estimator corresponding to the Chebyshev 
    polynomial of each degree. reports_by_degree is the report returned by system.compare().
    Input:
    - system: an instance of EstimatorSystem, which contains the noise model and the symbolic variables
    - chebyshev_degrees: a list of integers representing the degrees of the 
        Chebyshev polynomials to analyze
    Output:
    - reports_by_degree: a dictionary mapping each degree n to the report 
    generated by system.compare() for the Chebyshev polynomial of degree n. 
    Each report contains the symbolic expressions for the variance and MSE 
    of the unbiased estimator corresponding to the Chebyshev polynomial 
    of that degree.
    """
    reports_by_degree = {}
    for n in chebyshev_degrees:
        f = sp.chebyshevt(n, system.q)
        report = system.compare(f=f)
        reports_by_degree[n] = report

    return reports_by_degree

def build_reports(chebyshev_degrees):
    """
    Function uses the build_system functions to initialize the 
    system with the specified symbolic variables, and the calls symbolic_chebyshev_reports
    to compute the reports for the specified Chebyshev degrees.
    It basically combines the system initialization and the report generation.
    Input:
    - chebyshev_degrees: a list of integers representing the degrees of the Chebyshev polynomials to analyze
    Output:
    - reports_by_degree: a dictionary mapping each degree n to the report generated by system.compare()
    - symbols: a dictionary of the symbolic variables used in the system
    """
    system, symbols = build_system()
    reports_by_degree = symbolic_chebyshev_reports(
        system=system,
        chebyshev_degrees=chebyshev_degrees
    )
    return reports_by_degree, symbols

