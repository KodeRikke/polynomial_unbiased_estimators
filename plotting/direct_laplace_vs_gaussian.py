"""
Experiment a)
--- Fixed q, vary noise scale
--- Compare:
--- --- Var_unbiased Laplace
--- --- Var_unbiased Gaussian
--- --- Var_naive Laplace
--- --- Var_naive Gaussian
--- This reveals different tail behaviors of the noise distributions, 
    and how they affect the variance of the unbiased estimator, 
    compared to the naive estimator.

Experiment b)
--- Fixed noise scale, vary q
--- Reveals how the variance of the estimators changes 
    with the magnitude of the query output.

Experiment c)
--- Fixed polynomial, compare Laplace and Gaussian noise
--- This answers, which noise dist. yields better unbiased
    estimator performance for a given polynomial.

Repeat for MSE instead of variance, to see the bias-variance tradeoff.
"""
