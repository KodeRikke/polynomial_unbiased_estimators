def plot_plots(
    *,
    q_values,
    q_range,
    epsilon_values,
    epsilon_range,
    polynomials,
    Delta_values,
    metrics,
    each_plot,
    path_for_plots
):
    reports, symbols = build_reports(polynomials)

    eps_grid = np.linspace(epsilon_range[0], epsilon_range[1], 300)
    q_grid = np.linspace(q_range[0], q_range[1], 300)

    names = list(polynomials.keys())

    for metric in metrics:
        y_label = METRIC_LABELS[metric]

        if "direct_by_q_and_poly" in each_plot:
            for name in names:
                for q_value in q_values:
                    for Delta_value in Delta_values:
                        curves = curves_direct_for_fixed_poly_and_q(
                            reports=reports,
                            symbols=symbols,
                            name=name,
                            metric=metric,
                            q_value=q_value,
                            Delta_value=Delta_value,
                            eps_grid=eps_grid,
                        )

                        title = (
                            f"{y_label} vs ε for {format_value(name)}, "
                            f"q={format_value(q_value)}, "
                            f"Δ={format_value(Delta_value)}, "
                            f"variance-matched Gaussian"
                        )

                        save_path = os.path.join(
                            path_for_plots,
                            "direct_by_q_and_poly",
                            metric,
                        )

                        plot_curves(
                            x_label="ε",
                            y_label=y_label,
                            x_grid=eps_grid,
                            y_data=curves,
                            title=title,
                            save_path=save_path,
                            yscale="log"
                        )
        if "direct_by_epsilon_and_poly" in each_plot: 
            for name in names:
                for epsilon_value in epsilon_values:
                    for Delta_value in Delta_values:
                        curves = curves_direct_for_fixed_poly_and_epsilon(
                            reports=reports,
                            symbols=symbols,
                            name=name,
                            metric=metric,
                            epsilon_value=epsilon_value,
                            Delta_value=Delta_value,
                            q_grid=q_grid,
                        )

                        title = (
                            f"{y_label} vs q for {format_value(name)}, "
                            f"ε={format_value(epsilon_value)}, "
                            f"Δ={format_value(Delta_value)}, "
                            f"variance-matched Gaussian"
                        )

                        save_path = os.path.join(
                            path_for_plots,
                            "direct_by_epsilon_and_poly",
                            metric,
                        )

                        plot_curves(
                            x_label="q",
                            y_label=y_label,
                            x_grid=q_grid,
                            y_data=curves,
                            title=title,
                            save_path=save_path,
                        )
