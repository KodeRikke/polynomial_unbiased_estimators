def curves_direct_for_fixed_poly_and_q(
        reports,
        symbols,
        name,
        metric,
        q_value,
        Delta_value,
        eps_grid
):
    """
    One plot for a fixed polynomial and fixed q.
    Curves are:
        Laplace naive
        Laplace unbiased
        Gaussian naive
        Gaussian unbiased

    X-axis is epsilon.
    """
    out = {}
    epsilon_symbol = symbols["laplace"]["epsilon"]

    for noise_dist in ["laplace", "gaussian"]:
        for estimator in ["naive", "unbiased"]:
            expr = metric_expr_estimators(
                reports_by_label=reports[noise_dist],
                label=name,
                estimator=estimator,
                metric=metric,
            )

            subs = base_subs_for_noise(
                noise_dist=noise_dist,
                symbols=symbols,
                q_value=q_value,
                Delta_value=Delta_value,
            )

            label = f"{noise_dist.capitalize()} {estimator}"

            out[label] = evaluate_on_grid(
                expr,
                epsilon_symbol,
                eps_grid,
                subs,
            )

    return out

def curves_direct_for_fixed_poly_and_epsilon(
        reports, 
        symbols, 
        name, 
        metric,
        epsilon_value,
        Delta_value, 
        q_grid   
):
    """
    One plot for a fixed polynomial and fixed epsilon.
    Curves are:
        Laplace naive
        Laplace unbiased
        Gaussian naive
        Gaussian unbiased

    x-axis is q.
    """
    out = {}

    for noise_dist in ["laplace", "gaussian"]:
        q_symbol = symbols[noise_dist]["q"]

